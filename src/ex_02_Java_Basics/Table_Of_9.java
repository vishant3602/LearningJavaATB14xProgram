package ex_02_Java_Basics;

public class Table_Of_9 {
    public static void main(String[] args) {
        int n = 9;

        System.out.printf("%dx1=%d",n,n);
        System.out.println();
        System.out.printf("%dx2=%d",n,n*2);
        System.out.println();
        System.out.printf("%dx3=%d",n,n*3);
        System.out.println();
        System.out.printf("%dx4=%d",n,n*4);
        System.out.println();
        System.out.printf("%dx5=%d",n,n*5);
        System.out.println();
        System.out.printf("%dx6=%d",n,n*6).println();
        System.out.printf("%dx7=%d",n,n*7).println();
        System.out.printf("%dx8=%d",n,n*8).println();
        System.out.printf("%dx9=%d",n,n*9).println();
        System.out.printf("%dx10=%d",n,n*10);
    }
}
