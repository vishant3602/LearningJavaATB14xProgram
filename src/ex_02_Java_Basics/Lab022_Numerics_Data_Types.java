package ex_02_Java_Basics;

public class Lab022_Numerics_Data_Types {
    public static void main(String[] args) {

        byte b = 10;
        short s = 20;
        int i = 10;
        char c = 'A';

        long phone = 9876543210L;
        long phone1 = 9876543210l;

        // For long, we need to add L or l at the end.

        float f = 3.14F;
        float f1 = 3.14f;

        // For float, we need to add F or f at the end.

        double d = 3.1233456;

    }
}
